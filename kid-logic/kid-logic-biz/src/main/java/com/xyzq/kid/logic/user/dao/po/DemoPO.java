package com.xyzq.kid.logic.user.dao.po;

/**
 * 范例持久对象
 */
public class DemoPO {
    /**
     * 主键ID
     */
    private int id;
    /**
     * 内容
     */
    private String content;


    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getContent() {
        return content;
    }
    public void setContent(String content) {
        this.content = content;
    }
}
